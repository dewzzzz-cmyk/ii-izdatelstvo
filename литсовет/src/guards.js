// Разбирает пункты [РАЗБОР] вида «цитата» → ОТКЛОНЕНО: причина — Прозаик
// мотивированно отказался вносить правку (художественный приём). Эти пункты
// запоминаются на сцене (scene.rejectedNotes), чтобы то же замечание не
// подсвечивалось Стражами повторно и не гонялось по кругу в директиве.
function parseRejected(text){
  const out = [];
  // Ищем маркер «→ ОТКЛОНЕНО» и идём НАЗАД от него, считая глубину вложенности
  // «…», чтобы найти ПАРНУЮ открывающую кавычку — работает при ЛЮБОЙ глубине
  // вложенности (не только одном уровне). Раньше регэксп с одним уровнем
  // вложенности («(?:[^«»]|«[^»]*»)*») терял пункт целиком при 3+ уровнях
  // (модель цитирует фрагмент, который сам содержит цитату) — без ошибки, просто
  // пропуская запись, что могло зациклить директиву на одном и том же замечании.
  const marker = /→\s*ОТКЛОНЕНО:?\s*/gi;
  let m;
  while((m = marker.exec(text))){
    const before = text.slice(0, m.index);
    const closeIdx = before.lastIndexOf('»');
    if(closeIdx < 0) continue;
    let depth = 1, i = closeIdx - 1;
    for(; i >= 0; i--){
      if(text[i] === '»') depth++;
      else if(text[i] === '«'){ depth--; if(depth === 0) break; }
    }
    if(i < 0) continue;
    const quote = text.slice(i+1, closeIdx).trim();
    if(quote.length < 4) continue;
    const nl = text.indexOf('\n', marker.lastIndex);
    const reason = text.slice(marker.lastIndex, nl<0?undefined:nl).trim();
    out.push({ quote, reason });
  }
  return out;
}

// Вычленяет "спор" (РАЗБОР) и прозу (ТЕКСТ) из ответа surgicalReviseMessages.
// Возвращает { debate, prose, rejected } или { debate, prose:null, truncated:true, rejected }
// если модель обрезалась внутри [РАЗБОР] и не успела написать [ТЕКСТ].
// Теги [РАЗБОР]/[ТЕКСТ] допускают markdown-обвязку (**[ТЕКСТ]**), двоеточие
// после тега, пробелы внутри скобок и отсутствие переноса строки перед текстом —
// модель регулярно отклоняется от буквального формата, и раньше ЛЮБОЕ из этих
// отклонений давало ложное «обрезан токенами» с откатом к старому черновику,
// хотя токены были ни при чём (найдено консилиумом на 4/4 тестовых вариантах).
const TEXT_TAG_RE = /\**\[\s*ТЕКСТ\s*\]\**:?[ \t]*\n?/i;
const DEBATE_TAG_RE = /\**\[\s*РАЗБОР\s*\]\**/i;

export function parseDebateRevision(text){
  const rejected = parseRejected(text);
  const m = text.match(new RegExp(TEXT_TAG_RE.source + '([\\s\\S]+)', 'i'));
  const hasDebate = DEBATE_TAG_RE.test(text);
  const proseCandidate = m ? m[1].trim() : '';
  // Усечение: модель начала отвечать в формате [РАЗБОР]/[ТЕКСТ], но тега
  // [ТЕКСТ] либо нет вовсе, либо он есть, но почти без содержимого (обрыв по
  // токенам сразу после тега) — в обоих случаях нельзя использовать сырой
  // ответ как прозу: там окажется весь [РАЗБОР] с построчными вердиктами.
  if(hasDebate && proseCandidate.length <= 50){
    return { debate: text, prose: null, truncated: true, rejected };
  }
  let prose = m ? proseCandidate : text;
  // Артефакт хирургической правки: LLM копирует конец фразы с точкой и затем дописывает
  // продолжение через запятую. Результат: «целое., но» → убираем паразитную точку.
  prose = prose.replace(/\.,(\s)/g, ',$1');
  // Аналогично «!,» и «?,» — редко, но бывает
  prose = prose.replace(/([!?])\s*,(\s)/g, '$1$2');
  return m
    ? { debate: text.slice(0, m.index).trim(), prose, rejected }
    : { debate: '', prose, rejected };
}

