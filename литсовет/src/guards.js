// Строгость 1-3 → инструкция для Стража (реально влияет на число и порог флагов).
function strictnessLine(strictness){
  const s = strictness||2;
  if(s<=1) return 'Строгость: МЯГКАЯ — отмечай только грубые, явные проблемы; мелочи пропускай.';
  if(s>=3) return 'Строгость: ВЫСОКАЯ — придирайся, отмечай даже мелкие и потенциальные проблемы.';
  return 'Строгость: ОБЫЧНАЯ — отмечай заметные проблемы, не придирайся к мелочам.';
}

function parseFlags(text){
  const j = extractJSON(text);
  if(!j || !Array.isArray(j.flags)) return [];
  return j.flags.filter(f=>f&&f.title).map(f=>({
    severity: ['critical','warning','ok'].includes(f.severity)?f.severity:'warning',
    title: String(f.title).slice(0,120),
    detail: String(f.detail||'').slice(0,300),
    quote: f.quote? String(f.quote).slice(0,200):'',
  }));
}

// ── Детектор механических повторов фразы (НЕ LLM — быстрая проверка n-грамм). ──
// Ловит артефакты вроде повторно вставленного при правке куска текста
// («пятно на потолке, трещина на стене... пятно на потолке, трещина на стене»),
// не путая с естественным повтором имён/слов по всей сцене: флагует только
// длинные (6+ слов подряд) буквальные совпадения В БЛИЗКОЙ БЛИЗОСТИ друг к
// другу (окно DUP_MAX_GAP_CHARS) — случайная стыковка правки, а не сюжетный
// рефрен через полсцены, который может быть осознанным приёмом.
// 6, не 4: короткие 4-словные зачины ловили анафору («О том, как он [X]. О том,
// как он [Y].» — намеренный параллелизм, не артефакт) — найдено живым тестом.
// При 6 словах анафора расходится раньше конца шингла и не матчится, а настоящий
// дубль («пятно на потолке, трещина на стене», 6 слов) продолжает ловиться.
const DUP_SHINGLE_WORDS = 6;
const DUP_MAX_GAP_CHARS = 400;

export function findDuplicatePhrases(text){
  if(!text) return [];
  const re = /[а-яё]+/gi;
  const words = []; let m;
  while((m = re.exec(text))){ words.push({ w: m[0].toLowerCase(), idx: m.index, len: m[0].length }); }
  if(words.length < DUP_SHINGLE_WORDS*2) return [];

  const seen = new Map(); // shingle → индекс первого слова совпадения
  const ranges = [];
  for(let i=0; i<=words.length-DUP_SHINGLE_WORDS; i++){
    const key = words.slice(i, i+DUP_SHINGLE_WORDS).map(w=>w.w).join(' ');
    const prev = seen.get(key);
    if(prev!=null){
      const prevEnd = words[prev+DUP_SHINGLE_WORDS-1];
      const gap = words[i].idx - (prevEnd.idx + prevEnd.len);
      if(gap >= 0 && gap <= DUP_MAX_GAP_CHARS) ranges.push({ from:i, to:i+DUP_SHINGLE_WORDS-1 });
    } else {
      seen.set(key, i);
    }
  }
  if(!ranges.length) return [];

  // соседние/перекрывающиеся диапазоны слов (длинная фраза матчится несколькими
  // сдвинутыми окнами) объединяем в один читаемый фрагмент цитаты.
  ranges.sort((a,b)=>a.from-b.from);
  const merged = [];
  let cur = { ...ranges[0] };
  for(let i=1;i<ranges.length;i++){
    if(ranges[i].from <= cur.to+1) cur.to = Math.max(cur.to, ranges[i].to);
    else { merged.push(cur); cur = { ...ranges[i] }; }
  }
  merged.push(cur);

  return merged.map(r=>{
    const startChar = words[r.from].idx;
    const last = words[r.to];
    const endChar = last.idx + last.len;
    return { quote: text.slice(startChar, endChar).trim() };
  });
}

// ── Детектор дословного повтора СТЫКА между сценами — НЕ LLM, n-граммы. ──
// Живой инцидент: последняя фраза сцены «Первый вздох» («Я сунул ключ в
// карман... пошёл в сторону света...») дословно повторилась как ПЕРВАЯ фраза
// следующей сцены «Приёмная гильдии». Ни findDuplicatePhrases (сравнивает
// текст ТОЛЬКО сам с собой внутри одной сцены), ни crossSceneRepeats в
// pipeline.js (сравнивает только то, что сам Оценщик процитировал как
// клише — это сюжетное действие, не клише) этого не ловят: стык МЕЖДУ
// соседними сценами вообще не проверялся никем. Окно другое, чем у
// findDuplicatePhrases — не «где угодно рядом», а именно конец предыдущей
// сцены против начала текущей, поэтому нет смысла делить окно по BOUNDARY_WORDS
// на близость: любое совпадение шингла в этой узкой паре зон подозрительно.
const BOUNDARY_WORDS = 250;
export function findBoundaryRepeat(prevText, newText){
  if(!prevText || !newText) return [];
  const re = /[а-яё]+/gi;

  // Хвост предыдущей сцены — с грубой обрезкой по символам перед точным
  // подсчётом слов (сцены длинные, не гонять regex по всему тексту).
  const tailSlice = prevText.slice(Math.max(0, prevText.length - 4000));
  const tailWords = []; let m;
  while((m = re.exec(tailSlice))){ tailWords.push(m[0].toLowerCase()); }
  const tail = tailWords.slice(-BOUNDARY_WORDS);

  const headSlice = newText.slice(0, 4000);
  const headWords = []; re.lastIndex = 0;
  while((m = re.exec(headSlice))){ headWords.push({ w:m[0].toLowerCase(), idx:m.index, len:m[0].length }); }
  const head = headWords.slice(0, BOUNDARY_WORDS);

  if(tail.length < DUP_SHINGLE_WORDS || head.length < DUP_SHINGLE_WORDS) return [];

  const tailShingles = new Set();
  for(let i=0; i<=tail.length-DUP_SHINGLE_WORDS; i++){ tailShingles.add(tail.slice(i,i+DUP_SHINGLE_WORDS).join(' ')); }

  const ranges = [];
  for(let i=0; i<=head.length-DUP_SHINGLE_WORDS; i++){
    const shingle = head.slice(i,i+DUP_SHINGLE_WORDS).map(w=>w.w).join(' ');
    if(tailShingles.has(shingle)) ranges.push({ from:i, to:i+DUP_SHINGLE_WORDS-1 });
  }
  if(!ranges.length) return [];

  ranges.sort((a,b)=>a.from-b.from);
  const merged = [];
  let cur = { ...ranges[0] };
  for(let i=1;i<ranges.length;i++){
    if(ranges[i].from <= cur.to+1) cur.to = Math.max(cur.to, ranges[i].to);
    else { merged.push(cur); cur = { ...ranges[i] }; }
  }
  merged.push(cur);

  return merged.map(r=>{
    const startChar = head[r.from].idx;
    const last = head[r.to];
    const endChar = last.idx + last.len;
    return { quote: headSlice.slice(startChar, endChar).trim() };
  });
}

