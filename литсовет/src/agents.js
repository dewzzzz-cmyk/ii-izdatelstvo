      + (repeatPhrases.length ? '\n— дословные фразы (та же формулировка × кол-во раз, где угодно в сцене): ' + repeatPhrases.map(r=>`«${r.phrase}»×${r.count}`).join(', ') : '')
      + (tagBalance ? `\n— глагол-тег речи: «${tagBalance.stem}…» — ${tagBalance.count} из ${tagBalance.total} всех атрибуций реплик сцены (${Math.round(tagBalance.share*100)}%)` : '')
