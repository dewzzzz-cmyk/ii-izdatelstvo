    'ВАЖНО: там же — проверь ПРОПАЖУ: выпиши сущности, которые бриф называет прямо (персонажи, предметы, отношения между ними, заявленные события), и сверь каждую с черновиком. Сущность, которую черновик не упоминает вообще, — не «лаконичность», а потерянная опора сюжета: перечисли каждую пропавшую в notes как обязательную правку («бриф требует X — в тексте X отсутствует») и снижай ось «Соответствие брифу». Пропуск второстепенной краски брифа (цвет, погода) — не штраф; пропуск сущности, на которой стоит смысл сцены или следующих сцен, — штраф всегда.',
