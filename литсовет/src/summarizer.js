// Суммаризатор — сжатие завершённого после одобрения (спека 7).
// Сцена → ~60 слов + обновления состояний персонажей.
// Глава → ~200 слов + новые факты в Bible.
// Книга → ~300 слов.
// Промпты + парсеры; вызовы — в memory.js / pipeline.js.

import { extractJSON } from './llm.js';

// prevSummary — сводка (или хвост текста) НЕПОСРЕДСТВЕННО предыдущей сцены по
// порядку структуры/чтения, не всей книги — тот же принцип и источник, что у
// стража логики/событий (см. factsBlock в guards.js): минимум контекста на
// стыке сцен, чтобы верно судить о значимости сдвига (согласие — разворот, а
// не решение с нуля) и разрешать местоимения, но не гадать по устаревшему
// бегущему синопсису всей книги и не платить за него на каждой сцене.
export function sceneSummaryMessages(scene, sceneText, knownNames, prevSummary){
  const sys = 'Ты — архивариус. Сжимаешь сцену в краткую сводку для памяти книги. Пиши плотно, только факты и сдвиги состояния.';
  const namesNote = knownNames && knownNames.length
    ? `УЖЕ ИЗВЕСТНЫЕ ПЕРСОНАЖИ (используй ТОЧНО эти формы имени, если пишешь про них — не сокращай и не меняй форму, иначе один человек разделится на две разные карточки): ${knownNames.join(', ')}.`
    : '';
  const prevNote = prevSummary
    ? `НЕПОСРЕДСТВЕННО ПРЕДЫДУЩАЯ СЦЕНА (только для контекста — судить о значимости сдвигов в этой сцене и понимать, к кому относятся местоимения; не пересказывай её обратно в сводке):\n${prevSummary}`
    : '';
  const user = [
    'Бриф сцены (ПЛАН до написания — сцена могла при написании отклониться от него): ' + (scene.brief || scene.title || ''),
    namesNote,
    prevNote,
    '',
    'ТЕКСТ СЦЕНЫ (источник истины):',
    sceneText,
    '',
    'Верни JSON: { "summary": "сводка сцены, до 60 слов — что произошло, что изменилось", "characters": [{"name":"имя","state":"состояние/знание персонажа на конец сцены"}], "facts": [{"keys":"ключи через запятую","text":"новый канонический факт о мире/персонаже"}] }',
    'summary — сухо и по делу. characters — только те, чьё состояние реально изменилось. facts — только действительно новые факты (0-3). Если сцена впервые определяет природу или устройство важного повторяющегося объекта/механики мира (что это, как работает, чего не может) — обязательно занеси это фактом, даже если сцена не «про» этот объект: иначе при его следующем появлении в другой сцене сверить будет не с чем.',
    'ВАЖНО: бриф — это ЗАМЫСЕЛ, а не факт. И summary, и facts должны отражать ТОЛЬКО то, что реально написано в ТЕКСТЕ СЦЕНЫ. Если готовый текст разошёлся с брифом (другая деталь, другой персонаж, другой исход) — фиксируй то, что в тексте, а не то, что планировал бриф. Не заимствуй формулировки из брифа, если текст им не подтверждён.',
    'Только JSON.',
  ].filter(Boolean).join('\n');
  return [{role:'system',content:sys},{role:'user',content:user}];
}

export function parseSceneSummary(text){
  const j = extractJSON(text);
  if(!j) return null;
  return {
    summary: typeof j.summary==='string' ? j.summary.trim() : '',
    characters: Array.isArray(j.characters) ? j.characters.filter(c=>c&&c.name) : [],
    facts: Array.isArray(j.facts) ? j.facts.filter(f=>f&&f.text) : [],
  };
}

// Сверка нового факта канона против уже существующего факта той же темы (по
// сходству ключей/векторов, см. summarizeScene() в memory.js) — дополняет или
// противоречит? Вызывается ТОЛЬКО для пар с умеренным сходством (не дубли —
// те уже отсеяны раньше по высокому косинусу, и не разные темы — те вообще
// не похожи). Одним вызовом на сцену, а не на пару, чтобы не плодить запросы.
export function factConflictMessages(pairs){
  const sys = [
    'Ты — сверяешь новые факты канона книги против уже существующих фактов той же темы.',
    'Для каждой пары определи: НОВЫЙ факт дополняет СУЩЕСТВУЮЩИЙ (нормально, не флагуй) или противоречит ему (реальная нестыковка — напр. один факт называет предмет технологией, другой — магией).',
    'Не путай уточнение/развитие детали с противоречием — если оба факта могут быть верны одновременно, это не конфликт.',
  ].join('\n');
  const user = [
    'Пары фактов:',
    pairs.map((p,i)=>`${i+1}. НОВЫЙ: ${p.newText}\n   СУЩЕСТВУЮЩИЙ: ${p.oldText}`).join('\n\n'),
    '',
    'Верни JSON: { "conflicts": [{"index": номер пары (1-based), "explain": "в чём именно противоречие"}] }',
    'Только реальные противоречия. Непротиворечивые пары не включай. Если противоречий нет — верни пустой массив.',
    'Только JSON.',
  ].join('\n');
  return [{role:'system',content:sys},{role:'user',content:user}];
}

export function parseFactConflicts(text){
  const j = extractJSON(text);
  if(!j || !Array.isArray(j.conflicts)) return [];
  return j.conflicts.filter(c=>c && Number.isFinite(c.index)).map(c=>({
    index: c.index,
    explain: String(c.explain||'').slice(0,300),
  }));
}

