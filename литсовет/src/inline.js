// Точечная правка выделенного фрагмента (плавающее меню в редакторе).
// НЕ запускает полный пайплайн — меняет ТОЛЬКО выделенное, вписывая между
// «до» и «после». Результат сплайсится обратно в прозу на место выделения.

import { callLLM } from './llm.js';

const ACTIONS = {
  rewrite:   'Перепиши этот фрагмент иначе, сохранив смысл и его роль в сцене.',
  shorten:   'Сократи этот фрагмент, оставив только суть.',
  intensify: 'Усиль этот фрагмент — больше напряжения и конкретной чувственной детали, без лишних слов.',
  detail:    'Добавь в этот фрагмент конкретную деталь мира (предмет, звук, запах), не меняя сути.',
  continue:  'Продолжи прозу сразу после этого фрагмента. Верни ТОЛЬКО продолжение, не повторяя исходный текст.',
};

// Возвращает новый текст фрагмента (для continue — только продолжение).
export async function transformSelection(state, action, selected, before, after){
  const g = state.global;
  if(!g.apiKey) throw new Error('Не задан API-ключ.');
  const prose = (state.agents||[]).find(a=>a.role==='prose') || {};
  const sys = 'Ты — прозаик-редактор. Тебе дают ФРАГМЕНТ сцены и его окружение (текст до и после). Ты меняешь ТОЛЬКО фрагмент по инструкции так, чтобы он гладко вписался между «до» и «после». Ты НЕ переписываешь всю сцену и не трогаешь окружение. Сохраняй голос и стиль.';
  const user = [
    'ТЕКСТ ДО (не менять):\n' + (before || '(начало сцены)'),
    '',
    'ТЕКСТ ПОСЛЕ (не менять):\n' + (after || '(конец сцены)'),
    '',
    'ФРАГМЕНТ (только его и меняй):\n' + selected,
    '',
    'ИНСТРУКЦИЯ: ' + (ACTIONS[action] || ACTIONS.rewrite),
    'Верни ТОЛЬКО новый текст фрагмента, без кавычек, заголовков и пояснений.',
  ].join('\n');
  const res = await callLLM({
    baseURL:g.baseURL, apiKey:g.apiKey, model:g.model,
    temperature: prose.temp ?? 0.8, messages:[{role:'system',content:sys},{role:'user',content:user}],
    maxTokens: 700, retries: g.retries,
  });
  return res.text.trim();
}

export const INLINE_ACTIONS = [
  ['Переписать','rewrite'],
  ['Сократить','shorten'],
  ['Усилить','intensify'],
  ['Деталь мира','detail'],
  ['Продолжить','continue'],
];
