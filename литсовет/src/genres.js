export const HUMOR_GENRES = ['ироничный детектив', 'ироничное фэнтези', 'юмористическая проза'];
export function genreWantsHumor(genre){
  const g = (genre||'').toLowerCase();
  return HUMOR_GENRES.some(w=>g.includes(w));
}

