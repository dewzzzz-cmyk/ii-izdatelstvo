// Обязательные жанровые биты — общий 4-актный шаблон (завязка/развитие/
// кульминация/развязка) не учитывает специфику жанра, а у романтики и
// детектива/триллера есть устоявшиеся ожидания читателя, без которых книга
// ощущается «неполной» для жанра, независимо от качества прозы.
// Порядок веток важен: более специфичные жанры (ромфант, тёмная романтика,
// дамский детектив) проверяются РАНЬШЕ общих (романтика, детектив/триллер),
// иначе общая ветка перехватит их первой — их названия — подстроки общих
// («тёмная романтика» содержит «романтик», «дамский детектив» содержит «детектив»).
export function genreBeatsNote(genre){
  const g = (genre||'').toLowerCase();
  if(g.includes('ромфант')){
    return 'ЖАНРОВЫЕ БИТЫ (ромфант) — сочетай биты романтики и фэнтези, не выбирай одно в ущерб другому: мит-кьют на фоне уже действующего магического конфликта (не после него), нарастающее влечение, синхронное с эскалацией внешней угрозы/квеста, «чёрный момент» — одновременно романтический разрыв И критическая точка магического сюжета, воссоединение пары закрывает и romance-, и fantasy-линию. Магия/мир должны двигать сюжет (быть причиной решений героев), а не быть декорацией вокруг романтики. Финал — HEA или HFN, жанровое требование.';
  }
  if(g.includes('тёмная романтика') || g.includes('темная романтика')){
    return 'ЖАНРОВЫЕ БИТЫ (тёмная романтика) — та же основа, что у романтики (мит-кьют, эскалация близости, чёрный момент, воссоединение), но допускай моральную серость и токсичную динамику без принудительного смягчения (герой/героиня могут быть опасны, манипулятивны, антагонистичны по отношению друг к другу) — это ожидание жанра, не ошибка характера. НЕ выправляй токсичность в здоровые отношения искусственно ради «приятности» — читатель жанра идёт именно за интенсивностью. Финал — HEA или HFN остаётся жанровым требованием, но путь к нему может быть жёстче, чем в классической романтике.';
  }
  if(g.includes('любовн') || g.includes('романтик')){
    return 'ЖАНРОВЫЕ БИТЫ (романтика) — обязательно заложи в структуру сцены/главы под них: мит-кьют (первая встреча романтической пары, ранняя завязка), нарастающее влечение с эскалацией близости, «чёрный момент» (кульминационный конфликт-разрыв — кажется, что пара не может быть вместе), финальное воссоединение. Финал книги — HEA или HFN (однозначно счастливый или обнадёживающий) — это ЖАНРОВОЕ ТРЕБОВАНИЕ, не опция.';
  }
  if(g.includes('дамский детектив') || g.includes('cozy')){
    return 'ЖАНРОВЫЕ БИТЫ (дамский детектив/cozy) — честная расстановка улик и минимум одна красная сельдь, как в классическом детективе, НО: расследование ведёт не профессионал (сыщик-любитель со своей мотивацией и жизнью вне дела), насилие и жестокость — за кадром или сглажены, тон тёплый, с бытовыми/сообщественными деталями (не мрачный процедурал). Развязка логична из показанных улик, финал даёт эмоциональное разрешение и герою, и его личной сюжетной линии, не только раскрытие преступления.';
  }
  if(g.includes('детектив') || g.includes('триллер') || g.includes('мистик')){
    return 'ЖАНРОВЫЕ БИТЫ (детектив/триллер) — обязательно заложи: честную расстановку улик (читатель технически должен иметь возможность разгадать до развязки — ключевые улики нельзя утаивать от читателя, даже если они не привлекают внимания в моменте), минимум одну красную сельдь (правдоподобный ложный след), развязку, где объяснение логично следует из уже показанных улик, а не из внезапно введённого нового факта.';
  }
  if(g.includes('литрпг') || g.includes('игровая фантастика')){
    return 'ЖАНРОВЫЕ БИТЫ (ЛитРПГ) — герой явно существует в системе с измеримыми правилами (уровни, характеристики, навыки, лут) — заложи в структуру регулярные точки прогрессии: обретение/повышение способности как следствие сюжетного события (не случайно), хотя бы одно столкновение, где герой проигрывает из-за конкретного числового/механического недостатка (не абстрактной слабости характера), финальную кульминацию, которая решается применением прокачанной механики, а не роялем в кустах. Система должна быть частью логики мира, а не просто украшением текста цифрами.';
  }
  return '';
}

// Тон/тропы уровня сцены — что Прозаику уместно/неуместно делать в конкретном
// жанре, помимо общих анти-ИИ-инструкций. Та же логика веток и порядка, что у
// genreBeatsNote: специфичные жанры проверяются раньше общих.
export function genreToneNote(genre){
  const g = (genre||'').toLowerCase();
  if(g.includes('литрпг') || g.includes('игровая фантастика')){
    return 'ТОН ЖАНРА (ЛитРПГ): уместны игровые термины и элементы UI мира (всплывающие системные сообщения, панели характеристик, нотификации) прямо в теле сцены — это не нарушение «показывай, не рассказывай», а конвенция жанра. Числа и статы — не сухая справка, а источник эмоции (рост силы = катарсис читателя).';
  }
  if(g.includes('дамский детектив') || g.includes('cozy')){
    return 'ТОН ЖАНРА (дамский детектив): лёгкий, тёплый, местами ироничный тон даже в напряжённых сценах; насилие и его последствия — не смаковать, показывать сдержанно или за кадром. Бытовые детали (еда, интерьер, отношения в сообществе) — не балласт, а часть удовольствия от чтения.';
  }
  if(g.includes('тёмная романтика') || g.includes('темная романтика')){
    return 'ТОН ЖАНРА (тёмная романтика): не смягчай интенсивность и моральную неоднозначность ради «приятности» персонажей — читатель жанра ценит именно напряжение и опасность в отношениях. Физическое и эмоциональное влечение можно показывать прямо, без стыдливых умолчаний.';
  }
  if(g.includes('ромфант')){
    return 'ТОН ЖАНРА (ромфант): магия и романтика должны звучать в одном регистре — не переключайся резко между «эпичным» языком мира и «бытовым» языком чувств; они должны питать друг друга в одной сцене.';
  }
  return '';
}

// Заметка для КОНТРОЛЁРОВ качества (Оценщик, Стражи) — зеркало genreToneNote,
// но с другой адресацией: та разрешает Прозаику приём, эта просит судью не
// штрафовать за уже разрешённый приём. Без неё Оценщик/Стражи не знают о
// жанровых исключениях, которые получил Прозаик, и наказывают его за то, что
// сами же не помешали ему написать — трение, а не контроль качества.
export function genreJudgeNote(genre){
  const g = (genre||'').toLowerCase();
  if(g.includes('литрпг') || g.includes('игровая фантастика')){
    return 'ЖАНРОВАЯ ПОПРАВКА (ЛитРПГ): всплывающие системные сообщения, панели характеристик и явные числа-статы в теле сцены — жанровая конвенция автора, не абстракция и не нарушение «показывай, не рассказывай». Не штрафуй за это ось «Конкретность»/«Свежесть» и не считай логической дырой внезапное появление игрового интерфейса без сюжетной причины.';
  }
  if(g.includes('дамский детектив') || g.includes('cozy')){
    return 'ЖАНРОВАЯ ПОПРАВКА (дамский детектив/cozy): тон намеренно лёгкий и тёплый, напряжение ниже, чем в триллере, насилие — за кадром. Не требуй остроты триллера там, где жанр обещает уют — это не провал ставки, а жанровый контракт.';
  }
  if(g.includes('тёмная романтика') || g.includes('темная романтика')){
    return 'ЖАНРОВАЯ ПОПРАВКА (тёмная романтика): токсичная или морально серая динамика между персонажами — жанровое ожидание, а не непоследовательность характера или логическая ошибка сюжета. Не флагуй это как нелогичное поведение персонажа.';
  }
  if(g.includes('ромфант')){
    return 'ЖАНРОВАЯ ПОПРАВКА (ромфант): намеренное смешение эпического регистра (магия/мир) и интимного регистра (романтика) в одной сцене — жанровый приём, а не «разъехавшийся регистр» образа.';
  }
  return '';
}
